query = '''
SELECT p.user_id,
       p.device_type_canonical,
       p.order_id,
       p.created_dt_msk AS order_dt,
       p.created_ts_msk AS order_ts,
       p.currency_code,
       p.revenue,
       p.tickets_count,
      (p.created_dt_msk::DATE - LAG(p.created_dt_msk::DATE) OVER (PARTITION BY p.user_id ORDER BY p.created_dt_msk)) ::int AS days_since_prev,
      p.event_id,
      e.event_name_code AS event_name,
      e.event_type_main,
      p.service_name,
      r.region_name,
      c.city_name
FROM afisha.purchases p
JOIN afisha.events e ON p.event_id = e.event_id
JOIN afisha.city c ON c.city_id = e.city_id
JOIN afisha.regions r ON r.region_id = c.region_id 
WHERE p.device_type_canonical IN ('mobile', 'desktop') AND e.event_type_main <> 'фильм'
ORDER BY p.user_id ASC
'''


