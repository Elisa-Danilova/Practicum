segments = {
    name: df_filtered_95_copy[df_filtered_95_copy['orders_segment'] == name][cols]
    for name in ['1 заказ', '2–4 заказа', '5+ заказов']
}

interval_cols = ['avg_revenue_per_order', 'avg_tickets_per_order', 'avg_time_between']
correlations_by_segment = {}

for name, seg in segments.items():
    print(f"Считаю для: {name}")
    
    matrix = phik.phik_matrix(seg, interval_cols=interval_cols)
    
    if 'total_orders' in matrix.columns:
        correlations_by_segment[name] = matrix['total_orders'].sort_values(ascending=False)
        print("  → OK, корреляции посчитаны")
    else:
        correlations_by_segment[name] = None
        print(f"  → total_orders исключена в сегменте '{name}' (нет вариации — у всех почти одинаковое число заказов)")

print("\n--- Результаты ---")
for name, corr in correlations_by_segment.items():
    if corr is not None:
        print(f"\n{name}:")
        print(corr)
    else:
        print(f"\n{name}: корреляцию с total_orders посчитать нельзя (колонка константа)")


